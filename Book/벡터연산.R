# ------------------------------
# 벡터 연산
# ------------------------------

# %in% 연산자는 어떤 값이 벡터에 포함되어있는지를 알려준다.
14 %in% vector2 # TRUE
25 %in% vector2 # FALSE

# -----------------------------------------------------------------
# 벡터를 집합(set)으로 취급해 집합간 합집합, 교집합, 차집합을 계산
# -----------------------------------------------------------------
union(c("a" , "b" , "c" ), c("a" , "d" ))       # 합집합 : "a" "b" "c" "d"
intersect(c("a" , "b" , "c" ), c("a" , "d" ))   # 교집합 : "a"
setdiff(c("a" , "b" , "c" ), c("a" , "d" ))     # 차집합 : "b" "c"
#---------------------------------------
# 집합 비교
#---------------------------------------
setequal(c(1:5),c(1,2,3,4,5))   # TRUE
setequal(c(1:5),c(1,2,3,5))     # FALSE
#----------------------------------------------
# 벡터와 값의 연산 : 각각의 요소에 계산됨
#----------------------------------------------
v1 = 1:5
v1
v1 + 2
v1 - 2
v1 * 2
v1 / 2
v1 %% 2
v1 ^ 2
v1 < 10
v1 > 10
v1%%2 | T   # 요소
v1%%2 || T  # 전체
v1%%2 & T   # 요소
v1%%2 && T  # 전체

#----------------------------------------------
# 벡터와 벡터의 연산 : 각각의 요소에 계산됨
#----------------------------------------------
v2 = 6:10
v1
v2
v1 + v2
v1 - v2
v1 * v2
v1 / v2
v1 %% v2

